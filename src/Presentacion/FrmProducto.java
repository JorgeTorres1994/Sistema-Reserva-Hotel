    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        /*if (txtnumero.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un N√∫mero de Habitaci√≥n");
            txtnumero.requestFocus();
            return;
        }*/
        
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre para el producto");
            txtnombre.requestFocus();
            return;
        }
        
        if (txtdescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripci√≥n para el producto");
            txtdescripcion.requestFocus();
            return;
        }

        if (txtprecio_venta.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio de diario para el producto");
            txtprecio_venta.requestFocus();
            return;
        }

        vProducto dts = new vProducto();
        fProducto func = new fProducto();

        //int seleccionado = cbonumero_habitacion.getSelectedIndex();
        //dts.setNumero((String) cbonumero_habitacion.getItemAt(seleccionado));

        //seleccionado = cbopiso.getSelectedIndex();
        //dts.setPiso((String) cbopiso.getItemAt(seleccionado));

        dts.setNombre(txtnombre.getText());
        
        dts.setDescripcion(txtdescripcion.getText());
        
        int seleccionado = cbounidad_medida.getSelectedIndex();
        dts.setUnidad_medida((String) cbounidad_medida.getItemAt(seleccionado));
        
        dts.setPrecio_venta(Double.parseDouble(txtprecio_venta.getText()));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El producto fue registrado satisfactoriamente");
                mostrar("");
               inhabilitar();
                
            }
        }
        else if (accion.equals("editar")){
            dts.setIdProducto(Integer.parseInt(txtidproducto.getText()));
            
            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El producto fue editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtidproducto.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est√°s seguro de eliminar el producto?","Confirmar",2);
            
            if (confirmacion==0) {
                fProducto func = new fProducto ();
                vProducto dts= new vProducto();
                
                dts.setIdProducto(Integer.parseInt(txtidproducto.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            } 
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

